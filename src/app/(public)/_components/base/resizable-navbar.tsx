export const NavbarButton = ({
  href,
  as: Tag = Link,
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient" | "shimmer";
  [key: string]: any;
}) => {
  const baseStyles =
    "px-4 py-2 text-sm rounded-md font-bold relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-flex items-center justify-center gap-2 text-center";

  const variantStyles = {
    primary: "bg-primary text-primary-foreground shadow-lg hover:bg-primary/90",
    secondary:
      "bg-transparent text-foreground hover:bg-accent hover:text-accent-foreground",
    dark: "bg-foreground text-background shadow-lg",
    shimmer:
      "animate-shimmer bg-[linear-gradient(110deg,var(--color-primary),45%,color-mix(in_srgb,var(--color-primary),white_25%),55%,var(--color-primary))] bg-[length:200%_100%] text-primary-foreground transition-colors focus:outline-none hover:bg-[length:200%_100%] dark:bg-[linear-gradient(110deg,var(--color-primary),45%,color-mix(in_srgb,var(--color-primary),white_25%),55%,var(--color-primary))] dark:text-primary-foreground",
    gradient:
      "bg-gradient-to-b from-blue-500 to-blue-700 text-white shadow-[0px_2px_0px_0px_rgba(255,255,255,0.3)_inset]",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
